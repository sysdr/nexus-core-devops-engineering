//! NexusCore Day 35 — Host Orchestrator
//!
//! Manages N concurrent ReAct agent instances, each backed by a separate
//! Wasmtime Store (separate linear memory, separate fuel).
//! Uses FuturesUnordered for cooperative async scheduling — no OS thread per agent.

use anyhow::{anyhow, Context, Result};
use clap::Parser;
use futures::stream::{FuturesUnordered, StreamExt};
use serde::Serialize;
use std::path::PathBuf;
use std::sync::Arc;
use std::time::Instant;
use tokio::sync::{RwLock, Semaphore};
use wasmtime::component::{Component, Linker, ResourceTable};
use wasmtime::{Config, Engine, Store};
use wasmtime_wasi::{WasiCtx, WasiCtxBuilder, WasiView};
use wasmtime_wasi_http::{WasiHttpCtx, WasiHttpView};

use axum::extract::State;
use axum::http::Method;
use tower_http::cors::{Any, CorsLayer};

#[derive(Parser)]
#[command(name = "nexuscore", about = "NexusCore ReAct Agent Host")]
struct Cli {
    #[arg(short, long, default_value = "component/nexuscore_agent.wasm")]
    wasm: String,

    #[arg(short, long, default_value = "What is the latest Rust edition and what major features does it include?")]
    query: String,

    #[arg(short = 'n', long, default_value_t = 1)]
    concurrency: usize,

    #[arg(long, default_value = "https://api.anthropic.com/v1/messages")]
    llm_endpoint: String,

    #[arg(long, default_value = "http://localhost:8765")]
    search_endpoint: String,

    #[arg(long, env = "ANTHROPIC_API_KEY", default_value = "")]
    api_key: String,

    #[arg(long, default_value_t = 10)]
    max_steps: usize,

    #[arg(long, default_value_t = 8080)]
    metrics_port: u16,

    /// After each viz update, write JSON here so the static dashboard (port 9090) can read
    /// `./last-viz.json` when the host is not running (demo exits when the agent finishes).
    #[arg(long, default_value = "visualizer/last-viz.json")]
    viz_snapshot: PathBuf,
}

// ── WASI State ────────────────────────────────────────────────────────────────
struct AgentState {
    wasi: WasiCtx,
    http: WasiHttpCtx,
    table: ResourceTable,
}

impl WasiView for AgentState {
    fn ctx(&mut self) -> &mut WasiCtx { &mut self.wasi }
    fn table(&mut self) -> &mut ResourceTable { &mut self.table }
}
impl WasiHttpView for AgentState {
    fn ctx(&mut self) -> &mut WasiHttpCtx { &mut self.http }
    fn table(&mut self) -> &mut ResourceTable { &mut self.table }
}

